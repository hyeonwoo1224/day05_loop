package day05_loop;

public class Quiz08 {
	public static void main(String[] args) {
		for(int i=2;i<=9; i++) {
			for(int j=1; j<=9; j++) {
				System.out.println(i+"X"+j+"="+i*j);
			}
		}
		for(int i=1; i<=5; i++) {
			for(int j=1; j<=5; j++) {
				System.out.print(i*j+"\t");
			}
			System.out.println();
		}
	for(int i=1; i<=5; i++) {
		for(int j=1; j<=4; j++) {
			System.out.print(j+"\t");
		}
	}
	}
}
